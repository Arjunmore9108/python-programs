# 7. Number Expansion Calculator

# Write a Python program that accepts an integer (n) and computes the value of n+nn+nnn.
# Sample value of n is 5
# Expected Result : 615
#--------------------------------------------------------------------------------------------------------
n=input("enter n:")
n1=int(n*1)
n2=int(n*2)
n3=int(n*3)
result=n1+n2+n3
print(result)